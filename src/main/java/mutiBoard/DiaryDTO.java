package mutiBoard;

public class DiaryDTO {
	
	private String regiyear;
	private String regimonth;
	private String regidate;
	private String send_id;
	private String rece_id;
	private String content;
	private String flag;
	private int idx;
	
	
	public String getRegiyear() {
		return regiyear;
	}
	public void setRegiyear(String regiyear) {
		this.regiyear = regiyear;
	}
	public String getRegimonth() {
		return regimonth;
	}
	public void setRegimonth(String regimonth) {
		this.regimonth = regimonth;
	}
	public String getRegidate() {
		return regidate;
	}
	public void setRegidate(String regidate) {
		this.regidate = regidate;
	}
	public String getSend_id() {
		return send_id;
	}
	public void setSend_id(String send_id) {
		this.send_id = send_id;
	}
	public String getRece_id() {
		return rece_id;
	}
	public void setRece_id(String rece_id) {
		this.rece_id = rece_id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	
	
}
