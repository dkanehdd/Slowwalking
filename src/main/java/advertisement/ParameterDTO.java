package advertisement;

public class ParameterDTO {
	
	private String search;
	private String list_flag;
	private String request_time;
	private String regular_short;
	private String title;
	private String region;
	private String age;
	private String consultation;
	
	
	
	public String getConsultation() {
		return consultation;
	}
	public void setConsultation(String consultation) {
		this.consultation = consultation;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	public String getList_flag() {
		return list_flag;
	}
	public void setList_flag(String list_flag) {
		this.list_flag = list_flag;
	}
	public String getRequest_time() {
		return request_time;
	}
	public void setRequest_time(String request_time) {
		this.request_time = request_time;
	}
	public String getRegular_short() {
		return regular_short;
	}
	public void setRegular_short(String regular_short) {
		this.regular_short = regular_short;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	
	
	
}
